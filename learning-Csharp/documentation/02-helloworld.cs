using System;

class Hello
{
    static void Main()
    {
        Console.WriteLine("Hello, World");
    }
}

/*

Using (chamada de biblioteca) faz referência ao System que permite o uso de algumas 
classes e funções como o "Console", que permite o uso do WriteLine.

A classe Hello tem um único membro chamado de Main.

O método Main é declarado com o modificador static.

Métodos static operam sem referência a um objeto específico.

Por convenção, um método estático denomidado Main serve como ponto de entrada de um
programa em c#.


*/