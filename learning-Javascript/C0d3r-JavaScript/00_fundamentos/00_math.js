//* Math
//Math é um objeto embutido que tem propriedades e métodos para constantes e funções matemáticas.

console.log(Math.round(34.475)) // Arredonda o valor

console.log(Math.abs(-56)) // Retorna o valor positivo

console.log(Math.cbrt(27)) // Retorna a Raiz Cúbica

console.log(Math.cos(30)) // Retorna o coseno

console.log(Math.sin(13)) // Retorna o Seno

console.log(Math.tan(40)) // Retorna a tangente

console.log(Math.ceil(2.54)) // Retorna o menor inteiro que é maior ou igual a um número.

console.log(Math.floor(4.82)) // Retorna o maior inteiro que é menor ou igual a um número.

console.log(Math.sqrt(9)) // Retorna a raiz quadrada postiva de um número

console.log(Math.trunc(34.453)) // Retorna a parte inteira de um número

console.log(Math.pow(2, 5)) // Retorna a base x(2) elevada a potência de y(5)

console.log(Math.random()) // Retorna um número aleatório entre 0 e 1
